turtle.pen()
turtle.pencolor(1,0,0)
turtle.fillcolor('green')
turtle.speed(10)
for i in range(12):
    turtle.forward(i)
    turtle.right(i)
    turtle.left(200)
    turtle.forward(100)
    turtle.right(50)
    turtle.fillcolor('blue')
    turtle.left(56)
    turtle.back(112)
    turtle.right(90)

    turtle.right(500)
    turtle.fillcolor('green')
